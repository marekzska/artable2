#!/usr/bin/env node
/**
 * Export artworks from the artable API and save to src/data/artworks.ts
 * Usage:
 *   ARTWORKS_API_URL=https://your-api.com/artworks ARTWORKS_BASE_URL=https://your-images.com node scripts/export-artworks.mjs
 */

import { writeFileSync } from 'fs';
import { resolve } from 'path';

const API_URL = process.env.ARTWORKS_API_URL;
const BASE_URL = process.env.ARTWORKS_BASE_URL ?? '';

if (!API_URL) {
  console.error('Error: ARTWORKS_API_URL environment variable is required');
  process.exit(1);
}

const PERIODS = [
  'medieval_and_gothic',
  'renaissance',
  'mannerism_and_baroque',
  'rococo',
  'neoclassicism',
  'nineteenth_century',
  'modern',
];

async function fetchPeriod(period) {
  const url = `${API_URL}?period=${period}`;
  console.log(`Fetching ${period}...`);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for period "${period}"`);
  const data = await res.json();
  const artworks = (data.result ?? data).map(a => ({
    title: a.title,
    author: a.author,
    artmovement: a.artmovement,
    link: BASE_URL ? `${BASE_URL}${a.link}` : a.link,
    ...(a.year ? { year: a.year } : {}),
    copyright: a.copyright ?? 'Public domain',
    ...(a.bio ? { bio: a.bio } : {}),
  }));
  console.log(`  → ${artworks.length} artworks`);
  return artworks;
}

async function main() {
  const byPeriod = {};
  for (const period of PERIODS) {
    byPeriod[period] = await fetchPeriod(period);
  }

  const total = Object.values(byPeriod).reduce((n, a) => n + a.length, 0);
  console.log(`\nTotal: ${total} artworks across ${PERIODS.length} periods`);

  const output = `import { Artwork, PeriodKey } from './types';
import { shuffle } from '../utils/shuffle';

// Generated by scripts/export-artworks.mjs — do not edit manually.
export const artworksByPeriod: Record<Exclude<PeriodKey, 'all_periods'>, Artwork[]> = ${JSON.stringify(byPeriod, null, 2)};

export function getArtworksForPeriod(period: PeriodKey): Artwork[] {
  if (period === 'all_periods') {
    return shuffle(Object.values(artworksByPeriod).flat());
  }
  return artworksByPeriod[period] ?? [];
}
`;

  const outPath = resolve(process.cwd(), 'src/data/artworks.ts');
  writeFileSync(outPath, output, 'utf8');
  console.log(`\nWritten to ${outPath}`);
  console.log('Run `npm run build` to bundle the new data.');
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
